import 'package:flutter/foundation.dart';
import 'base_model.dart';

abstract class BaseViewModel extends BaseModel {
  bool _disposed = false;

  @override
  void dispose() {
    _disposed = true;
    super.dispose();
  }

  @override
  void notifyListeners() {
    if (!_disposed) {
      super.notifyListeners();
    }
  }
}
